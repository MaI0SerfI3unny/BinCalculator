def binplus(bin_one, bin_two):
    result = (bin(bin_one + bin_two))
    print(result)

def binmin(bin_one, bin_two):
    result = (bin(bin_one - bin_two))
    print(result)

def binmn(bin_one, bin_two):
    result = (bin(bin_one * bin_two))
    print(result)

def bindl(bin_one, bin_two):
    result = (bin(bin_one / bin_two))
    print(result)
